              <p>绿野资本集团的核心业务是以自由贸易园区为龙头、产业地产为主体、全球投资服务和离岸金融服务为两翼：</p>
<p>1、<strong>绿野世界</strong><strong>自由贸易</strong><strong>业务集群</strong></p>
<p>绿野世界自由贸易业务集群秉承产业生态圈投资理念，聚集于自由贸易区产业的全产业链业务，包括：</p>
<p>A、 自由贸易区规划咨询<br />
B、 自由贸易区土地储备<br />
C、 自由贸易区投资开发<br />
D、 自由贸易区运营管理</p>
<p>目前已经在规划、投资、建设及运营的自由贸易区相关产业包括：</p>
<p>A、 加勒比自由贸易区：规划覆盖加勒比海的15个国家，第一期重点开发安提瓜园区、格林纳达园区、圣基茨园区、多米尼克园区；</p>
<p>B、 地中海自由贸易区：规划覆盖5个国家，第一期重点开发塞浦路斯园区、希腊园区；</p>
<p>C、 北美自由贸易区：规划覆盖美国、加拿大两个国家，第一期重点开发加利福尼亚州优胜美地园区、华盛顿州伯林翰园区；</p>
<p>D、 迪拜投资服务中心：规划覆盖迪拜28个自由贸易区，第一期重点服务杰拜勒.阿里自由区(Jebel Ali Free Zone)、迪拜机埸自由区(Dubai Airport Free Zone)；</p>
<p>2、<strong>绿野全球投资服务</strong><strong>业务集群</strong></p>
<p>绿野全球投资服务业务集群是经有关国家批准，联合所在国家的政府部门、投资项目方、律师会计师发起组建、专门为投资者提供投资服务的专业投资促进和服务机构，包括四大板块：</p>
<p>A、 <strong>投资促进服务：</strong>目前主要服务于加勒比海地区的圣基茨和尼维斯、安提瓜和巴布达、多米尼克国、格林纳达，阿联酋的迪拜、欧洲的希腊、塞浦路斯。</p>
<p>B、 <strong>海外上市服务：</strong>目前主要提供美国NASDAQ市场的SPAC反向并购上市、香港联合交易所上市。</p>
<p>C、 <strong>北美土地投资：</strong>目前主要提供美国土地储备投资服务、高尔夫度假村土地投资服务。</p>
<p>D、 <strong>海外房产投资：</strong>目前主要服务于加勒比海圣基茨和尼维斯、安提瓜和巴布达、多米尼克、格林纳达，阿联酋迪拜、地中海希腊和塞浦路斯、美国和加拿大的投资性物业、居住物业的投资服务。</p>
<p>3、<strong>绿野离岸金融服务</strong><strong>业务集群</strong></p>
<p>绿野离岸金融服务业务集群包括三大板块：</p>
<p>A、 <strong>金融机构业务：</strong>发起和收购商业银行、保险公司、信托机构、融资租赁公司；申请银行、保险、信托、博彩经营牌照；</p>
<p>B、 <strong>离岸基金业务：</strong>发起和管理自由贸易园区投资基金、好莱坞电影基金、旅游地产投资基金、土地储备投资基金；</p>
<p>C、 <strong>离岸公司服务：</strong>设立离岸豁免公司、开设离岸银行账号、设立离岸家族信托和投资基金，设立离岸银行、离岸保险公司、离岸信托公司；</p>
<p>4、<strong>绿野产业地产服务</strong><strong>业务集群</strong></p>
<p>绿野产业地产服务业务集群包括三大板块：</p>
<p>A、 <strong>世界自由贸易中心</strong>：为全球100个办公楼宇和商务园区提供冠名和资产管理服务，第一期已经开发和签约20余栋楼宇和商务园区；</p>
<p>B、 <strong>海外上市加速器</strong>：通过聚集和统领各类上市中介服务机构，如投资银行、会计师事务所、律师事务所、PRE-IPO投资基金、财经公关公司等为准上市企业提供全方位、一站式的上市服务；</p>
<p>C、 <strong>国际高尔夫度假村</strong>：规划投资开发覆盖美国、圣基茨、塞浦路斯、希腊、澳大利亚、加拿大的六个高端高尔夫度假村，第一期重点开发美国优胜美地高尔夫度假村、圣基茨和尼维斯联邦基茨山庄高尔夫度假村、塞浦路斯维纳斯之石高尔夫度假村；</p>
<p>D、 <strong>亚马逊电商物流园</strong>：规划覆盖中国50城市，第一期已经开发哈尔滨园区、武汉园区、西安园区。</p>
